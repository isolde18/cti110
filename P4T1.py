#CTI

import turtle

turtle.shape("turtle")

turtle.pensize(3)            # increase pensize (takes integer)
turtle.pencolor("purple")     # set pencolor (takes string)

for i in range(4):
    turtle.pencolor("purple") 

    turtle.forward(50)
    turtle.left(90)




turtle.penup()
turtle.forward(100)     
turtle.pendown()


